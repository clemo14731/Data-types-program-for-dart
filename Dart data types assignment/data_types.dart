void main() {
  // Integer data type
  int age = 25;
  print('Age: $age years');

  // Double data type
  double weight = 65.5;
  print('Weight: $weight kg');

  // String data type
  String name = 'Clement Juma';
  print('Name: $name');

  // List data type
  List<String> fruits = ['Apple', 'Banana', 'Orange', 'Pineapple'];
  print('Fruits: $fruits');

  // Map data type
  Map<String, int> studentScores = {
    'Joseph': 85,
    'Alice': 90,
    'Bob': 75,
    'Clement': 93,
  };
  print('Student Scores: $studentScores');
}
